#[derive(Debug)]
pub enum LexToken<'a> {
    Break,
    Match,
    FatArrow,
    SkinnyArrow,
    If,
    Else,
    Enum,
    For,
    In,
    Return,
    Type,
    While,
    Loop,
    Plus,
    PlusEq,
    PlusPlus,
    Minus,
    MinusEq,
    MinusMinus,
    Div,
    DivEq,
    Mult,
    MultEq,
    Mod,
    ModEq,
    Gt,
    Lt,
    Gte,
    Lte,
    Eq,
    EqEq,
    NotEq,
    LogAnd,
    LogOr,
    LogNot,
    BitAnd,
    BitAndEq,
    BitOr,
    BitOrEq,
    BitNot,
    BitXor,
    BitXorEq,

    Ident(&'a str),
    String(&'a str),
    Char(char),
    Int(u64),
    Float(f64),
}

#[derive(Debug)]
pub struct LexTokenData<'a> {
    token: LexToken<'a>,
    line_num: usize,
    col_num: usize,
    raw: &'a str,
}

